/**
 * Normalize a user-supplied URL, allowing only http(s) schemes. Returns the
 * canonical href, or null for empty/invalid input or any other scheme. This is
 * the authoritative gate: the demo/repo URLs are later rendered as <a href>,
 * and Svelte does NOT strip dangerous schemes, so a stored `javascript:` URL
 * would be a stored XSS against reviewers and the public explore page. The
 * client-side prefixing in +page.svelte is convenience only and bypassable.
 */
function cleanUrl(raw: FormDataEntryValue | null): string | null {
	const v = (raw as string | null)?.trim();
	if (!v) return null;
	let u: URL;
	try {
		u = new URL(v);
	} catch {
		return null;
	}
	return u.protocol === 'http:' || u.protocol === 'https:' ? u.href : null;
}

// Channel reviewers watch to pick up new submissions. Overridable via env.
const REVIEW_CHANNEL_ID = env.SLACK_REVIEW_CHANNEL_ID || 'C0BBHACUXE1';

// Boba Drops tracks. Individual submissions pay cash to the teen and are US-only;
// workshop submissions route the grant to the club leader and are worldwide.
const SUBMISSION_TYPES = ['individual', 'workshop'] as const;
type SubmissionType = (typeof SUBMISSION_TYPES)[number];

function isSubmissionType(v: unknown): v is SubmissionType {
	return typeof v === 'string' && (SUBMISSION_TYPES as readonly string[]).includes(v);
}

// Whether a stored country value counts as the United States. Accepts the common
// spellings/codes; case- and punctuation-insensitive.
function isUnitedStates(country: string | null | undefined): boolean {
	if (!country) return false;
	const c = country.trim().toLowerCase().replace(/\./g, '');
	return [
		'us',
		'usa',
		'united states',
		'united states of america',
		'u s a',
		'u s'
	].includes(c);
}

