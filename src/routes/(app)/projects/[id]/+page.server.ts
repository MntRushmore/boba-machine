/**
 * Post to the reviewer channel when a project is submitted or reshipped, so
 * reviewers know there's new work in the queue. Best-effort: never throws, and
 * is skipped on staging so test instances don't spam the real channel.
 */
async function notifyReviewChannel(opts: {
	origin: string;
	projectId: number;
	projectName: string;
	submitterSlackId: string | null;
	submitterName: string | null;
	submittedSeconds: number | null;
	submissionType?: string | null;
	isReship: boolean;
	repoUrl?: string | null;
	demoUrl?: string | null;
}) {
	if (isStaging()) return;
	try {
		const reviewUrl = `${opts.origin}/projects/${opts.projectId}`;
		const who = opts.submitterSlackId
			? `<@${opts.submitterSlackId}>`
			: opts.submitterName || 'Someone';
		const verb = opts.isReship ? 'reshipped' : 'shipped';
		const track = opts.submissionType ? ` _(${opts.submissionType})_` : '';
		const lines = [`:tea: *${who}* ${verb} *${opts.projectName}*${track}`];
		if (opts.repoUrl) lines.push(`• Repo: ${opts.repoUrl}`);
		if (opts.demoUrl) lines.push(`• Site: ${opts.demoUrl}`);
		lines.push(`<${reviewUrl}|Review on the dashboard>`);
		await sendSlackDM(REVIEW_CHANNEL_ID, lines.join('\n'));
	} catch (e) {
		console.error('failed to post submission notification to review channel', e);
	}
}

async function getHackatimeSeconds(
	dbUser: typeof users.$inferSelect,
	projectNames: string[]
): Promise<number> {
	if (!projectNames.length) return 0;
	if (!dbUser.hackatimeTokenCt || !dbUser.hackatimeTokenIv || !dbUser.hackatimeTokenTag) return 0;

	const encKey = encryptionKey();
	const accessToken = decryptToken(
		dbUser.hackatimeTokenCt,
		dbUser.hackatimeTokenIv,
		dbUser.hackatimeTokenTag,
		encKey
	);

	const res = await fetch(`${HACKATIME_BASE_URL}/api/v1/authenticated/projects`, {
		headers: { Authorization: `Bearer ${accessToken}` }
	});
	if (!res.ok) return 0;

	const data = await res.json();
	const raw: any[] = Array.isArray(data) ? data : (data.data ?? data.projects ?? []);
	const nameSet = new Set(projectNames);
	return raw
		.filter((p: any) => nameSet.has(String(p.name ?? '')))
		.reduce((sum: number, p: any) => sum + Number(p.total_seconds ?? p.totalSeconds ?? 0), 0);
}

async function getLatestApproval(projectId: number) {
	const [row] = await db
		.select()
		.from(projectApprovals)
		.where(eq(projectApprovals.projectId, projectId))
		.orderBy(desc(projectApprovals.submittedAt))
		.limit(1);
	return row ?? null;
}

export async function load({ locals, params }) {
	if (!locals.user) redirect(302, '/login');

	const id = parseInt(params.id, 10);
	if (isNaN(id)) error(404, 'project not found');

	const dbUser = await getDbUser(locals.user.sub);
	if (!dbUser) redirect(302, '/login');

	const [project] = await db.select().from(projects).where(eq(projects.id, id)).limit(1);

	if (!project || (!locals.isReviewer && project.userId !== dbUser.id)) {
		redirect(302, '/projects?error=not_found');
	}

	const approvals = await db
		.select({
			id: projectApprovals.id,
			status: projectApprovals.status,
			submittedSeconds: projectApprovals.submittedSeconds,
			approvedSeconds: projectApprovals.approvedSeconds,
			publicMessage: projectApprovals.publicMessage,
			internalNote: projectApprovals.internalNote,
			submittedAt: projectApprovals.submittedAt,
			reviewedAt: projectApprovals.reviewedAt,
			reviewerName: users.name,
			reviewerNickname: users.nickname,
			reviewerAvatar: users.slackAvatarUrl
		})
		.from(projectApprovals)
		.leftJoin(users, eq(projectApprovals.reviewerId, users.id))
		.where(eq(projectApprovals.projectId, id))
		.orderBy(asc(projectApprovals.submittedAt));

	// Legacy hours delta — Boba Drops no longer credits hours, but the field is kept
	// (always 0) so the returned shape stays stable for any UI still referencing it.
	const approvalsWithDelta = approvals.map((a) => ({ ...a, newSeconds: 0 }));

	// soft_approved is internal-only — non-reviewers/admins see it as still pending.
	// Relabel (rather than hide) the soft_approved approval so the author lands on the
	// pending view; hiding it would expose a stale approved/rejected status and wrongly
	// surface the reship/submit view while final confirmation is outstanding.
	const isInternal = locals.isReviewer || locals.isAdmin;
	const visibleApprovals = isInternal
		? approvalsWithDelta
		: approvalsWithDelta.map((a) => {
				if (a.status === 'soft_approved') {
					return {
						...a,
						status: 'pending',
						approvedSeconds: null,
						publicMessage: null,
						internalNote: null,
						reviewedAt: null,
						reviewerName: null,
						reviewerNickname: null,
						reviewerAvatar: null
					};
				}
				return { ...a, internalNote: null };
			});

	const latestApproval =
		visibleApprovals.length > 0 ? visibleApprovals[visibleApprovals.length - 1] : null;
	const derivedStatus: string | null = latestApproval?.status ?? null;

	const events = await db
		.select({
			id: projectEvents.id,
			action: projectEvents.action,
			message: projectEvents.message,
			internalNote: projectEvents.internalNote,
			createdAt: projectEvents.createdAt,
			actorName: users.name,
			actorNickname: users.nickname,
			actorAvatar: users.slackAvatarUrl
		})
		.from(projectEvents)
		.innerJoin(users, eq(projectEvents.actorId, users.id))
		.where(eq(projectEvents.projectId, id))
		.orderBy(asc(projectEvents.createdAt));

	const linkedRows = await db
		.select({ hackatimeProject: projects.hackatimeProject })
		.from(projects)
		.where(and(ne(projects.id, id), eq(projects.userId, dbUser.id)));
	const linkedHackatimeProjects = linkedRows.flatMap((r) =>
		r.hackatimeProject
			? r.hackatimeProject
					.split(',')
					.map((s) => s.trim())
					.filter(Boolean)
			: []
	);

	const [projectOwnerRow] = await db
		.select({
			name: users.name,
			nickname: users.nickname,
			avatar: users.slackAvatarUrl,
			slackId: users.slackId,
			slackDisplayName: users.slackDisplayName,
			verificationStatus: users.verificationStatus,
			yswsEligible: users.yswsEligible,
			yswsCheckResult: users.yswsCheckResult,
			birthday: users.birthday
		})
		.from(users)
		.where(eq(users.id, project.userId))
		.limit(1);

	// Reviewer-only panel data: Slack handle, ID verification status, and the
	// current YSWS eligibility decision for the project's author. Only ever sent
	// to reviewers/admins so we don't leak it back to the owner's own view.
	const ownerReviewInfo =
		isInternal && projectOwnerRow
			? (() => {
					const result = (projectOwnerRow.yswsCheckResult as CheckResult | null) ?? null;
					const decision = decideEligibility(result, projectOwnerRow.birthday);
					return {
						slackId: projectOwnerRow.slackId,
						slackDisplayName: projectOwnerRow.slackDisplayName,
						verificationStatus: projectOwnerRow.verificationStatus,
						yswsCheckResult: result,
						yswsEligible: projectOwnerRow.yswsEligible,
						// age is admin-only; reviewers see eligibility but not the raw age
					age: locals.isAdmin ? calculateAge(projectOwnerRow.birthday) : null,
						eligibilityDecision: decision,
						eligible: isEligibleDecision(decision)
					};
				})()
			: null;

	const visibleEvents = isInternal
		? events
		: events
				.filter((e) => !INTERNAL_ONLY_EVENT_ACTIONS.includes(e.action))
				.map(({ internalNote: _n, ...rest }) => ({ ...rest, internalNote: null }));

	return {
		project,
		approvals: visibleApprovals,
		events: visibleEvents,
		latestApproval,
		derivedStatus,
		// Legacy field retained for back-compat with the project page; hours are gone.
		availableSeconds: 0,
		linkedHackatimeProjects,
		projectOwner: projectOwnerRow
			? {
					name: projectOwnerRow.name,
					nickname: projectOwnerRow.nickname,
					avatar: projectOwnerRow.avatar
				}
			: null,
		ownerReviewInfo,
		isReviewer: locals.isReviewer,
		isAdmin: locals.isAdmin,
		isOwnProject: project.userId === dbUser.id,
		githubConnected: !!locals.user.github_login
	};
}

